package umu.tds.appchat.dominio;

/**
 * Enumeración que representa el tipo de un mensaje dentro de la aplicación.
 * Indica si un mensaje fue enviado o recibido por el usuario actual.
 * 
 * @author Ángel
 * @author Francisco Javier
 */
public enum TipoMensaje {

    /**
     * Mensaje enviado por el usuario actual.
     */
    ENVIADO,

    /**
     * Mensaje recibido por el usuario actual.
     */
    RECIBIDO;
}
